package whileloop;  //Assignment 36 = Program to demonstrate WHILE LOOP

public class While 
{

	public static void main(String[] args)
	{
		int age = 0;    // Declaration/ Intilization
		while (age<5)    // condition
		{
			System.out.println(age);   //logic
			age++;                     // increment/decrement
		}
	
	}

}
