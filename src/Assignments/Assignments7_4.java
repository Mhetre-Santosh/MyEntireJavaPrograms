package Assignments; // Print even and Odd using if else condition

public class Assignments7_4 
{

	public static void main(String[] args)
	{
		for (int i = 1; i<=100; i++)
		{
			if (i%2 == 0)
			{
				System.out.println("Even number "+i);
			}
			else
			{
				System.out.println("Odd number "+i);
			}
		}
	}

}
